	public static String getSpacedText(ParserRuleContext prc){
		if (prc.getChildCount() == 0) {
			return "";
		} else {
			StringJoiner builder = new StringJoiner(" ");
			for(int i = 0; i < prc.getChildCount(); ++i) {
				builder.add(prc.getChild(i).getText());
			}
			return builder.toString();
		}
	}
